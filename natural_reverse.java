
import java.util.*;

public class natural_reverse {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        System.out.println("enter number: ");
        int n = sc.nextInt();
        int i = n;
        while (i >= 1) {

            System.out.println(i);
            i--;
        }

    }

}
